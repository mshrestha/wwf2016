                <h1 class="uppercase">Forest management in critical corridors</h1>
                <p>Local communities are at the helm of forest management works in the critical corridors of TAL and mountain forests of SHL through activities such as plantation, grassland management and grazing control. The community forestry program in forest corridors, bottlenecks and buffer zone areas is the primary intervention through which local communities are directly involved in the sustainable management of community forests and natural resources that creates a balance between nature and people. The program transfers the use rights of forests from the government to the local communities thereby promoting their access to and rights over the sustainable use of forest resources.</p>
