						<div class="animate-visible"><h1 class="uppercase">ABOUT WWF NEPAL</h1></div>
						<div class="animate-visible">
							<p>Since 1961, WWF has worked to conserve nature and ecological processes through a combination of actions on the ground, national and international advocacy work to establish appropriate policies, and international campaigns to highlight and demonstrate solutions to crucial environmental problems.</p>
							<p>WWF started working in Nepal from 1967 when it launched a rhino conservation program in Chitwan. To keep up with the evolving face of conservation and the environmental movement, WWF’s focus evolved from its localized efforts in conservation of single species in the 1960s/1970s, integrated conservation and development approach in the 1980s, to a new horizon of landscape level conservation encompassing national, regional and global scales of complexity in the early 2000s.</p>
							<p>WWF’s work in Nepal is focused in the Terai Arc Landscape (TAL) and Sacred Himalayan Landscape (SHL), including Koshi River Basin, and Chiwan-Annapurna Landscape (CHAL) under the USAID-funded Hariyo Ban Program. It is centered on four thematic goals – forests, wildlife, water and climate & energy – and two drivers – finance and governance. The effective delivery of conservation results under the above four thematic areas are supported by crosscutting programs on policy and advocacy, curbing illegal wildlife trade, sustainable livelihoods, and communications.</p>
							<p class="">In Nepal, WWF works closely with the Ministry of Forests and Soil Conservation through the Department of National Parks and Wildlife Conservation and Department of Forests, Ministry of Population and Environment, Ministry of Agricultural Development, Ministry of Land Reform and Management, Water and Energy Commission Secretariat and National Trust for Nature Conservation. Besides the national priority areas, WWF Nepal also works in conservation issues of regional and trans-boundary importance.</p>
						</div>




						<div class="animate-visible"><h1 class="uppercase">Vision</h1></div>
						<div class="animate-visible">
							<p>WWF Nepal envisions a prosperous Nepal with a society possessing an ethic of stewardship and responsibility towards nature.</p>
							<p>By 2050 Nepal will have:</p>
							<ul class="list list-disc">
							    <li>Conserved biodiversity and the natural processes that sustain it in the Global 200 Ecoregions within Nepal</li>
							    <li>Established social and economic development patterns that assure the sustainable and equitable provision of natural goods and services, improving livelihoods and quality of life for current and future generations</li>
							    <li>Eliminated or mitigated critical threats to species, habitats, and ecological processes that derive from climate change, over exploitation of resources, unsustainable consumption, and pollution</li>
							</ul>
						</div>

						<div class="animate-visible"><h1 class="uppercase">Mission</h1></div>
						<div class="animate-visible">
							<p>WWF Nepal’s mission is to stop the degradation of Nepal’s natural environment, and to build a future in which people live in harmony with nature, by conserving biological diversity, ensuring sustainable use of renewable resources, reducing pollution and wasteful consumption, and addressing sustainable livelihoods.</p>
						</div>




