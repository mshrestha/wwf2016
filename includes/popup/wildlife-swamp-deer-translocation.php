			                <h1 class="uppercase">Swamp deer translocation</h1>
			                <p>Five swamp deer were successfully translocated to Bardia National Park from Shuklaphanta Wildlife Reserve in Nepal’s Terai Arc Landscape on 27 May–2 June 2016. Shuklaphanta Wildlife Reserve is the stronghold of Nepal’s swamp deer population presently 2,300 in number.With the support from the USAID-funded Hariyo Ban Program together with the Government of Nepal, the translocation expedition was carried out to enhance genetic robustness of the remnant swamp deer population of Bardia National Park, and establish a viable population of the species. An additional 30 swamp deer will be translocated between the two national parks till 2018.</p>
